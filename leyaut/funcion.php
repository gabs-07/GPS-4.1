        <h2> GPS Comunicaciones.</h2>

        <p>
            GPS Comunicaciones ofrece soluciones avanzadas de geolocalización satelital, diseñadas para responder a las necesidades 
            de monitoreo y control de flotas de vehículos en tiempo real. Con un enfoque en la innovación y calidad, sus servicios 
            permiten a las empresas mantener un control integral sobre sus unidades, optimizando la seguridad y eficiencia en sus 
            operaciones logísticas.
        </p>

        <p>
            Los sistemas de GPS de la empresa ofrecen funciones de seguridad como detección de arranque y movimiento no autorizados, 
            alertas de velocidad y corte de antena, así como opciones avanzadas de administración mediante mensajes de texto y acceso 
            web. Además, cuentan con funcionalidades adicionales como un micrófono ambiental y un botón de pánico, proporcionando un 
            nivel extra de seguridad y comunicación en situaciones críticas.
        </p>
    </div>

    <div class="sistemaBox">
        <h2>Sistema de disminución de riesgos.</h2>

        <ul>
            <li>Los equipos cuentan con un sistema de corte de corriente para paro de motor a distancia, que puede ser activado o desactivado de forma segura en el momento que se requiera por medio de un simple comando vía SMS.</li>
            <li>Todos nuestros equipos cuentan con la opción de botón de pánico o asistencia, el cual puede enviar alertas vía SMS a los números celulares autorizados una vez que sea oprimido por 3 segundos.</li>
            <li>Como valor agregado, nuestros equipos cuentan con la opción de micrófono ambiental, con sensibilidad ajustable, que puede ser activado a voluntad por medio de un comando SMS.</li>
        </ul>
    </div>

