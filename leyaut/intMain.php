            <h2>GPS Comunicaciones.</h2>

            <p>Es una aplicación cliente-servidor, que mediante el uso del sistema GPS (Global Positioning System), 
            permite conocer la posición de un vehículo en tiempo real.</p>

            <p>Esto es posible mediante una comunicación desde el cliente, instalado en el vehículo, hasta el servidor, 
            donde se procesa la señal, lo que permite mostrar dicha posición en un mapa.</p>

            <p>Toda empresa requiere conocer con exactitud dónde se encuentran sus vehículos para poder otorgar un 
            servicio eficiente, sin retrasos y con garantía, lo que le permite ser competitivo.</p>
            
            <p>Aquí es donde entra en juego la gestión de flotillas. A través de GPS Comunicaciones, se puede tener 
